async function main() {
  for (const t of tasks) {
    await prisma.task.upsert({
      where: { id: t.id },
      update: {},
      create: {
        id: t.id,
        prompt: t.prompt,
        responseA: t.responseA,
        responseB: t.responseB,
        category: t.category,
        isGold: t.isGold,
        goldAnswer: t.goldAnswer,
      },
    });
  }

  const total = await prisma.task.count();
  const gold = await prisma.task.count({ where: { isGold: true } });
  console.log(`Seeded ${total} tasks (${gold} gold)`);

  const existingAdmin = await prisma.adminUser.findUnique({
    where: { email: "admin@centient.work" },
  });
  if (!existingAdmin) {
    const password = process.env.ADMIN_SEED_PASSWORD ?? "GoCent!123";
    await prisma.adminUser.create({
      data: {
        email: "admin@centient.work",
	role: "SUPER_ADMIN",
        passwordHash: await bcrypt.hash(password, 10),
      },
    });
    console.log("Seeded admin user 'admin@centient.work'");
  } else {
    console.log("Admin user 'admin@centient.work' already exists — leaving untouched");
  }
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
